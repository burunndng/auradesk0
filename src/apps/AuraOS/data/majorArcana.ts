export function drawRandomCard(): MajorArcanaCard {
  return MAJOR_ARCANA[Math.floor(Math.random() * MAJOR_ARCANA.length)];
}

export function getCardByNumber(n: number): MajorArcanaCard | undefined {
  return MAJOR_ARCANA.find((c) => c.number === n);
}

export function getContemplatedCards(ids: string[]): MajorArcanaCard[] {
  return MAJOR_ARCANA.filter((c) => ids.includes(c.id));
}
